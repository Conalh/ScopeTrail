#!/usr/bin/env node
// ScopeTrail benchmark runner.
//
// Runs the built CLI's `diff` against every labeled fixture in
// benchmark/labels.json, then scores the verdicts against ground truth:
// precision, recall, and false-positive rate at each CI gate threshold,
// per-kind detection accuracy, and exact-rating agreement where the label
// pins a rating.
//
//   npm run build           # produce dist/ (the CLI under test)
//   node benchmark/run-benchmark.mjs
//
// Writes benchmark/RESULTS.md and prints the same report to stdout.
// Exit code is non-zero only if a fixture fails to run — the metrics
// themselves are reported, not gated.

import { execFileSync } from 'node:child_process';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');
const cli = join(repoRoot, 'dist', 'index.js');
const fixturesRoot = join(here, 'fixtures');

const SEVERITY_RANK = { none: 0, low: 1, medium: 2, high: 3, critical: 4 };
// 'low' == "any finding at all" (rating rises to at least low on a single hit).
const THRESHOLDS = ['low', 'medium', 'high', 'critical'];

if (!existsSync(cli)) {
  process.stderr.write(`CLI not found at ${cli}\nRun "npm run build" first.\n`);
  process.exit(1);
}

const corpus = JSON.parse(readFileSync(join(here, 'labels.json'), 'utf8'));

function runDiff(fixture) {
  const caseDir = join(fixturesRoot, fixture);
  const args = [
    'diff',
    '--old',
    join(caseDir, 'old'),
    '--new',
    join(caseDir, 'new'),
    '--format',
    'json'
  ];
  const stdout = execFileSync('node', [cli, ...args], {
    encoding: 'utf8',
    maxBuffer: 20 * 1024 * 1024
  });
  return JSON.parse(stdout);
}

const results = [];
let runErrors = 0;

for (const c of corpus.cases) {
  let report;
  try {
    report = runDiff(c.fixture);
  } catch (err) {
    runErrors += 1;
    process.stderr.write(`ERROR diffing ${c.fixture}: ${err.message}\n`);
    continue;
  }
  const kinds = report.findings.map((f) => f.kind);
  const kindSet = new Set(kinds);
  const primaryKind = c.expectKinds[0] ?? null;
  results.push({
    fixture: c.fixture,
    category: c.category,
    expectFlagged: c.expectFlagged,
    expectKinds: c.expectKinds,
    expectRating: c.expectRating,
    rating: report.rating,
    ratingRank: SEVERITY_RANK[report.rating],
    findingCount: report.findings.length,
    kinds,
    primaryMatched: primaryKind ? kindSet.has(primaryKind) : null,
    allKindsMatched: c.expectKinds.length ? c.expectKinds.every((k) => kindSet.has(k)) : null,
    ratingExact: c.expectRating ? report.rating === c.expectRating : null
  });
}

function confusionAt(threshold) {
  const rank = SEVERITY_RANK[threshold];
  let tp = 0,
    fp = 0,
    fn = 0,
    tn = 0;
  for (const r of results) {
    const predictedPositive = r.ratingRank >= rank;
    if (r.expectFlagged) {
      if (predictedPositive) tp += 1;
      else fn += 1;
    } else {
      if (predictedPositive) fp += 1;
      else tn += 1;
    }
  }
  const precision = tp + fp === 0 ? 1 : tp / (tp + fp);
  const recall = tp + fn === 0 ? 1 : tp / (tp + fn);
  const fpr = fp + tn === 0 ? 0 : fp / (fp + tn);
  const f1 = precision + recall === 0 ? 0 : (2 * precision * recall) / (precision + recall);
  const accuracy = (tp + tn) / (tp + fp + fn + tn);
  return { threshold, tp, fp, fn, tn, precision, recall, fpr, f1, accuracy };
}

const pct = (x) => `${(x * 100).toFixed(1)}%`;

const rogue = results.filter((r) => r.expectFlagged);
const benign = results.filter((r) => !r.expectFlagged);
const metrics = THRESHOLDS.map(confusionAt);
const detect = metrics.find((m) => m.threshold === 'low'); // any-finding detection
const gate = metrics.find((m) => m.threshold === 'high'); // typical CI gate

const primaryMatched = rogue.filter((r) => r.primaryMatched).length;
const allKindsMatched = rogue.filter((r) => r.allKindsMatched).length;
const ratingPinned = results.filter((r) => r.expectRating);
const ratingExact = ratingPinned.filter((r) => r.ratingExact).length;

// Distinct detector kinds the corpus exercises.
const expectedKinds = new Set();
for (const r of rogue) for (const k of r.expectKinds) expectedKinds.add(k);

// ---- surprises: ground-truth disagreements worth eyeballing ----
const missedRogue = rogue.filter((r) => !r.allKindsMatched);
const flaggedBenign = benign.filter((r) => r.findingCount > 0);
const ratingMisses = ratingPinned.filter((r) => !r.ratingExact);

// per-category breakdown
const categories = [...new Set(results.map((r) => r.category))].sort();

// ---------------------------- render ----------------------------
const lines = [];
const p = (s = '') => lines.push(s);

p('# ScopeTrail benchmark results');
p('');
p('Generated by `node benchmark/run-benchmark.mjs` against the built `dist/` CLI.');
p('Corpus: labeled fixtures in `benchmark/labels.json`, each pointing at an old/new');
p('snapshot pair under `benchmark/fixtures/`. Ground truth (intent, expected finding');
p('kinds, expected consolidated rating) is fixed by fixture design — the runner diffs');
p('the pair and scores the drift engine against it. Benign cases include deliberate');
p('false-positive traps a naive textual diff would flag (tightened posture, removed');
p('servers, reordered JSON keys).');
p('');
p(`- Cases: **${results.length}** (${rogue.length} rogue, ${benign.length} benign) across **${expectedKinds.size}** detector kinds`);
p(`- Detection (any finding): recall **${pct(detect.recall)}**, false-positive rate **${pct(detect.fpr)}**, precision **${pct(detect.precision)}**`);
p(`- At a \`fail-on: high\` CI gate: recall **${pct(gate.recall)}**, false-positive rate **${pct(gate.fpr)}**, precision **${pct(gate.precision)}**`);
p(`- Correct primary finding kind identified on **${primaryMatched}/${rogue.length}** rogue cases; all expected kinds on **${allKindsMatched}/${rogue.length}**`);
p(`- Exact rating match where the label pins one: **${ratingExact}/${ratingPinned.length}**`);
p('');

p('## Confusion matrix by CI gate threshold');
p('');
p('A PR is predicted "drift" when its overall rating meets the threshold. `low` = any finding at all.');
p('');
p('| Gate (`fail-on`) | TP | FP | FN | TN | Precision | Recall | FP rate | F1 | Accuracy |');
p('| --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: |');
for (const m of metrics) {
  p(
    `| ${m.threshold} | ${m.tp} | ${m.fp} | ${m.fn} | ${m.tn} | ${pct(m.precision)} | ${pct(m.recall)} | ${pct(m.fpr)} | ${pct(m.f1)} | ${pct(m.accuracy)} |`
  );
}
p('');

p('## Detector-kind identification (rogue cases)');
p('');
p(`- Primary expected kind detected: **${primaryMatched}/${rogue.length}**`);
p(`- All expected kinds detected: **${allKindsMatched}/${rogue.length}**`);
p(`- Distinct detector kinds exercised: **${expectedKinds.size}**`);
p('');

p('## Rating agreement');
p('');
p(`Of the **${ratingPinned.length}** cases whose label pins an exact consolidated rating, the diff matched **${ratingExact}**.`);
p('');

p('## Results by category');
p('');
p('| Category | Cases | Rogue detected | Benign clean |');
p('| --- | ---: | :---: | :---: |');
for (const cat of categories) {
  const inCat = results.filter((r) => r.category === cat);
  const rg = inCat.filter((r) => r.expectFlagged);
  const bn = inCat.filter((r) => !r.expectFlagged);
  const rgHit = rg.filter((r) => r.allKindsMatched).length;
  const bnClean = bn.filter((r) => r.findingCount === 0).length;
  p(`| ${cat} | ${inCat.length} | ${rg.length ? `${rgHit}/${rg.length}` : '—'} | ${bn.length ? `${bnClean}/${bn.length}` : '—'} |`);
}
p('');

p('## Disagreements with ground truth');
p('');
if (missedRogue.length || flaggedBenign.length || ratingMisses.length) {
  if (missedRogue.length) {
    p(`**Rogue cases missing an expected kind: ${missedRogue.length}**`);
    for (const r of missedRogue) p(`- ${r.fixture} — expected ${r.expectKinds.join(', ')}; got ${r.kinds.join(', ') || '(none)'}`);
    p('');
  }
  if (flaggedBenign.length) {
    p(`**False positives (benign, flagged): ${flaggedBenign.length}**`);
    for (const r of flaggedBenign) p(`- ${r.fixture} — emitted ${r.kinds.join(', ')}`);
    p('');
  }
  if (ratingMisses.length) {
    p(`**Rating mismatches: ${ratingMisses.length}**`);
    for (const r of ratingMisses) p(`- ${r.fixture} — expected ${r.expectRating}, got ${r.rating}`);
    p('');
  }
} else {
  p('None. Every rogue case produced all of its expected finding kinds, no benign case produced any finding, and every pinned rating matched.');
  p('');
}

p('## Per-case detail');
p('');
p('| Fixture | Intent | Category | Rating | Findings | Primary kind | All kinds | Rating |');
p('| --- | --- | --- | --- | ---: | :---: | :---: | :---: |');
for (const r of results) {
  const intent = r.expectFlagged ? 'rogue' : 'benign';
  const primary = r.expectKinds.length === 0 ? '—' : r.primaryMatched ? 'yes' : 'NO';
  const allK = r.expectKinds.length === 0 ? '—' : r.allKindsMatched ? 'yes' : 'NO';
  const ratingCell = r.expectRating ? (r.ratingExact ? 'yes' : 'NO') : '—';
  p(`| ${r.fixture} | ${intent} | ${r.category} | ${r.rating} | ${r.findingCount} | ${primary} | ${allK} | ${ratingCell} |`);
}
p('');

p('---');
p('');
p('Reproduce: `npm run build && node benchmark/run-benchmark.mjs`. Ground-truth');
p('labels live in `benchmark/labels.json`; each fixture is an `old/`+`new/` config');
p('snapshot pair the runner diffs with `scopetrail diff`.');

const out = lines.join('\n');
process.stdout.write(out + '\n');
writeFileSync(join(here, 'RESULTS.md'), out + '\n', 'utf8');

if (runErrors > 0) {
  process.stderr.write(`\n${runErrors} fixture(s) failed to run.\n`);
  process.exit(1);
}
